				else if (ch == '>') {buf.Append(ch); NextCh(); goto case 195;}
				else {t.kind = 2; goto done;}
			case 233:
				if ((ch >= '0' && ch <= '9')) {buf.Append(ch); NextCh(); goto case 2;}
				else if (ch == '>') {buf.Append(ch); NextCh(); goto case 193;}
				else {t.kind = 2; goto done;}
			case 234: {t.kind = 0; goto done;}
		}
		done:
		t.val = buf.ToString();
		return t;
	}

} // end Scanner


